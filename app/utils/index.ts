export function numToFa(index: number) {
    if (index === 0)
        return 'اول'
    else if (index === 1)
        return 'دوم'
    else if (index === 2)
        return 'سوم'
    else if (index === 3)
        return 'چهارم'
    else if (index === 4)
        return 'پنجم'
    else if (index === 5)
        return 'ششم'
}